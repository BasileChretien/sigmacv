/** What a profile link resolves to: which glyph, and (for known services) a
 *  canonical display label used to auto-name a bare URL. */
export interface ResolvedLink {
  icon: IconName;
  /** Canonical service name (e.g. "GitHub"), only for allowlisted hosts. */
  service?: string;
}

/**
 * Curated host allowlist → {icon, service}. Ordered; first match wins. ONLY these
 * services show a brand mark / get an auto-label; everything else falls back to the
 * generic "link" glyph and no service label (so a label can never claim a service
 * the URL doesn't actually point to). Google Scholar maps to the NEUTRAL academic
 * glyph, not Google's logo.
 */
const HOST_RULES: ReadonlyArray<{
  test: (host: string) => boolean;
  icon: IconName;
  service: string;
}> = [
  {
    test: (h) => h === "github.com" || h.endsWith(".github.com"),
    icon: "github",
    service: "GitHub",
  },
  { test: (h) => h === "linkedin.com", icon: "linkedin", service: "LinkedIn" },
  { test: (h) => h === "orcid.org", icon: "orcid", service: "ORCID" },
  { test: (h) => h.startsWith("scholar.google."), icon: "scholar", service: "Google Scholar" },
  { test: (h) => h === "x.com" || h === "twitter.com", icon: "x", service: "X" },
  {
    test: (h) => h === "researchgate.net" || h.endsWith(".researchgate.net"),
    icon: "researchgate",
    service: "ResearchGate",
  },
  { test: (h) => h === "youtube.com" || h === "youtu.be", icon: "youtube", service: "YouTube" },
  {
    test: (h) => h === "bsky.app" || h === "bsky.social" || h.endsWith(".bsky.social"),
    icon: "bluesky",
    service: "Bluesky",
  },
  // Mastodon is federated (no canonical host); a `mastodon.*` prefix is a
  // best-effort heuristic — unknown instances honestly fall back to the generic glyph.
  {
    test: (h) => h === "mastodon.social" || h.startsWith("mastodon."),
    icon: "mastodon",
    service: "Mastodon",
  },
];

/**
 * Resolve a user-supplied URL to an icon + optional service label. Reads the URL's
 * HOST ONLY — it never trusts, returns, or mutates the URL itself (the caller still
 * runs every href through `safeHref` + `escapeHtml`). Total: any parse failure or
 * unknown host returns the generic `link` glyph, never throws.
 */
export function resolveLink(url: string): ResolvedLink {
  let host: string;
  try {
    // Mirror safeHref's scheme-completion so a bare "github.com/x" parses, but the
    // result is DISCARDED — only the hostname is read.
    const withScheme = /^[a-z][a-z0-9+.-]*:\/\//i.test(url) ? url : `https://${url}`;
    host = new URL(withScheme).hostname.toLowerCase();
  } catch {
    return { icon: "link" };
  }
  host = host.replace(/^(www|m|mobile)\./, "");
  for (const rule of HOST_RULES) {
    if (rule.test(host)) return { icon: rule.icon, service: rule.service };
  }
  return { icon: "link" };
}
