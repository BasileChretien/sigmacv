/**
 * Public-page showcase style — "Hanko" (atmosphere redesign).
 *
 * A Franco-Japanese editorial style, rendered as a hanging scroll in a quiet
 * alcove. The mood lives in the ATMOSPHERE LAYER (a warm washi field with a soft
 * sumi-e ink-wash in one corner); the text lives on a separate READING SURFACE —
 * a warm paper page that floats above it — so nothing is ever behind a glyph.
 *
 *   • ATMOSPHERE — warm washi with a single soft sumi ink-wash in one top corner;
 *     a very slow breathe, confined to the surround.
 *   • READING SURFACE — a warm paper page, centred, floating on a soft shadow + a
 *     fine gold keyline, with generous ma (space).
 *   • TATEGAKI MARGIN (the signature) — a vertical 履歴書 column runs down the
 *     page's left margin in faint sumi, beside a thin rule.
 *   • CARVED SEAL — the lone saturated note: a vermilion hanko (印) stamped once
 *     beside your name.
 *   • TYPE — Newsreader (bundled), a literary high-contrast serif with refined
 *     italics.
 *   • SELF-NAME — your own (identifier-matched, never name-string-matched) name in
 *     sumi under a fine vermilion rule.
 *
 * One orchestrated load moment: the page settles open + the seal presses in.
 * CSS-ONLY; AA throughout (sumi ~14:1, vermilion #b23a2c ~5:1); reduced-motion
 * stills everything; print = clean black-on-white. Not a mascot style.
 */
