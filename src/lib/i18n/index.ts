import type { CvSectionType, NotMineReason } from "@/lib/canonical/schema";

/**
 * Lightweight, dependency-free internationalization.
 *
 * Two distinct concerns live here:
 *  1. The chrome dictionary (`t`) — the authenticated editor UI strings.
 *  2. CV-content localization — default section titles + not-mine reason labels
 *     that appear in the document the user builds.
 *
 * Everything is keyed off the user's chosen `display.locale`, which also drives
 * citeproc's citation language. English is the source of truth; every other
 * locale must define the SAME keys (TypeScript enforces this via the typed
 * Record maps below), so a missing string is impossible at runtime — `t` still
 * falls back to English defensively.
 */

export const SUPPORTED_LOCALES = [
  "en-US",
  "zh-CN",
  "es-ES",
  "fr-FR",
  "de-DE",
  "ja-JP",
  "pt-BR",
  "it-IT",
  "ko-KR",
  "ru-RU",
] as const;
export type Locale = (typeof SUPPORTED_LOCALES)[number];
export const DEFAULT_UI_LOCALE: Locale = "en-US";

/** Native language names for the locale picker. */
export const LOCALE_LABELS: Record<Locale, string> = {
  "en-US": "English",
  "zh-CN": "简体中文",
  "es-ES": "Español",
  "fr-FR": "Français",
  "de-DE": "Deutsch",
  "ja-JP": "日本語",
  "pt-BR": "Português",
  "it-IT": "Italiano",
  "ko-KR": "한국어",
  "ru-RU": "Русский",
};

/** Narrow an arbitrary stored locale string to a supported UI locale. */
export function asLocale(value: string | undefined): Locale {
  return (SUPPORTED_LOCALES as readonly string[]).includes(value ?? "")
    ? (value as Locale)
    : DEFAULT_UI_LOCALE;
}

// ─── URL locale slugs (for the multilingual, crawlable landing) ──────────────

/** Short URL slug per locale: en-US -> "en", zh-CN -> "zh", … (used as /[slug]). */
export const LOCALE_SLUGS: Record<Locale, string> = {
  "en-US": "en",
  "zh-CN": "zh",
  "es-ES": "es",
  "fr-FR": "fr",
  "de-DE": "de",
  "ja-JP": "ja",
  "pt-BR": "pt",
  "it-IT": "it",
  "ko-KR": "ko",
  "ru-RU": "ru",
};

/** The slug for the default locale ("en"); the default landing lives at "/". */
export const DEFAULT_LOCALE_SLUG = LOCALE_SLUGS[DEFAULT_UI_LOCALE];

/** Slugs for every NON-default locale — the set of `/[locale]` static params. */
export const NON_DEFAULT_LOCALE_SLUGS: string[] = SUPPORTED_LOCALES.filter(
  (l) => l !== DEFAULT_UI_LOCALE,
).map((l) => LOCALE_SLUGS[l]);

/** Slug for a locale (falls back to the default locale's slug). */
export function slugForLocale(locale: string): string {
  return LOCALE_SLUGS[asLocale(locale)];
}

/** Locale for a URL slug, or undefined if the slug is unknown. */
export function localeForSlug(slug: string): Locale | undefined {
  return (Object.entries(LOCALE_SLUGS) as [Locale, string][]).find(([, s]) => s === slug)?.[0];
}

/** Bare language code for a locale (the `inLanguage` / `lang` value): "fr-FR" → "fr". */
export function localeLanguageCode(locale: string): string {
  // Every supported locale is `xx-YY` with a two-letter language subtag.
  return asLocale(locale).slice(0, 2);
}

